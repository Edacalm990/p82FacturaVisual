    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarActionPerformed
        
        LocalDate fechaFactura = convertirFecha(fecha.getText());
        double importeFactura = convertirImporte(importe.getText());
        // si los datos son del formato correcto
        // damos por hecho que el importe de la factura nunca ser치 0
        if (convertirFecha(fecha.getText())!=null && importeFactura != 0) {
            Factura factura = new Factura();
            factura.setCodigoUnico(facturaTmp.getCodigoUnico());
            factura.setDescripcion(descripcion.getText());
            factura.setFechaEmision(Date.valueOf(fechaFactura));
            factura.setTotalImporte(importeFactura);
            try {
                controladorFactura.edit(factura);
                View vista= new View();
                vista.setVisible(true);
                vista.setLocationRelativeTo(null);
                dispose();
            } catch (Exception e) {
                System.out.println(e);
                Error error = new Error("Error: No se ha podido hacer la modificaci칩n, vuelva a interntarlo'");
                error.setVisible(true);
                error.setLocationRelativeTo(null);
            }
        }


    }//GEN-LAST:event_guardarActionPerformed

    private LocalDate convertirFecha(String fechaTexto) {
        // si falla es que no tiene el formato correcto y lanzar치 un error
        try {
            return LocalDate.parse(fechaTexto, DateTimeFormatter.ofPattern("dd/MM/yyyy"));
        } catch (Exception e) {
            Error error = new Error("Error: la fecha debe tener el formato 'dd/mm/yyyy'");
            error.setVisible(true);
        }
        return null;
    }

    private double convertirImporte(String importeTexto) {
        // si falla es que no tiene el formato correct y dar치 error
        try {
            // podemos poner un . y una ,
            return Double.parseDouble(importeTexto.contains(",") ? importeTexto.replace(",", ".") : importeTexto);
        } catch (Exception e) {
            Error error = new Error("Error: Debe ser un n칰mero'");
            error.setVisible(true);
        }
        return 0;
    }

